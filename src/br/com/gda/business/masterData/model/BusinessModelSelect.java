package br.com.gda.business.masterData.model;

import java.sql.Connection;

import javax.ws.rs.core.Response;

import br.com.gda.business.masterData.info.BusinessInfo;
import br.com.gda.business.masterData.model.decisionTree.RootBusinessSelect;
import br.com.gda.common.DbConnection;
import br.com.gda.common.DbSchema;
import br.com.gda.model.Model;
import br.com.gda.model.ModelHelper;
import br.com.gda.model.ModelOption;
import br.com.gda.model.decisionTree.DeciTree;
import br.com.gda.model.decisionTree.DeciTreeFactory;
import br.com.gda.model.decisionTree.DeciTreeOption;

public final class BusinessModelSelect implements Model {
	private ModelHelper<BusinessInfo> helper;
	private Connection conn;
	private String schemaName;
	
	
	public BusinessModelSelect(BusinessInfo recordInfo) {
		initialize();
		buildHelper(recordInfo);
	}
	
	
	
	private void initialize() {
		this.conn = DbConnection.getConnection();
		this.schemaName = DbSchema.getDefaultSchemaName();
	}
	
	
	
	private void buildHelper(BusinessInfo recordInfo) {
		ModelOption<BusinessInfo> helperOption = new ModelOption<>();
		
		helperOption.infoRecordClass = BusinessInfo.class;
		helperOption.decisionTreeFactory = new TreeFactory();
		helperOption.conn = this.conn;
		helperOption.schemaName = this.schemaName;
		
		helper = new ModelHelper<>(helperOption, recordInfo);
	}


	
	@Override public boolean executeRequest() {
		return helper.executeRequest();
	}


	
	@Override public Response getResponse() {
		return helper.getResponse();
	}
	
	
	
	
	
	
	private static class TreeFactory implements DeciTreeFactory<BusinessInfo> {		
		@Override public DeciTree<BusinessInfo> getInstance(DeciTreeOption<BusinessInfo> option) {
			return new RootBusinessSelect(option);
		}		
	
	}
}
