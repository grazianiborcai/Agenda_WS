package br.com.mind5.payment.refundOrder.model.checker;

import br.com.mind5.masterData.language.info.LanguInfo;
import br.com.mind5.masterData.language.model.checker.LanguCheckExist;
import br.com.mind5.model.checker.ModelCheckerOption;
import br.com.mind5.model.checker.ModelCheckerTemplateForwardV2;
import br.com.mind5.model.checker.ModelCheckerV1;
import br.com.mind5.payment.refundOrder.info.RefuInfo;

public final class RefuCheckLangu extends ModelCheckerTemplateForwardV2<RefuInfo, LanguInfo> {
	
	public RefuCheckLangu(ModelCheckerOption option) {
		super(option);
	}


	
	@Override protected ModelCheckerV1<LanguInfo> getCheckerHook(ModelCheckerOption option) {
		return new LanguCheckExist(option);
	}
	
	
	
	@Override protected LanguInfo toForwardClass(RefuInfo baseRecord) {
		return LanguInfo.copyFrom(baseRecord);
	}
}
