package br.com.mind5.business.bankAccount.model.decisionTree;

import java.util.ArrayList;
import java.util.List;

import br.com.mind5.business.bankAccount.info.BankaccInfo;
import br.com.mind5.business.bankAccount.model.action.BankaccVisiMergeToUpdatePartner;
import br.com.mind5.business.bankAccount.model.action.BankaccVisiRootUpdate;
import br.com.mind5.business.bankAccount.model.checker.BankaccCheckUpdatePartner;
import br.com.mind5.model.action.ActionLazy;
import br.com.mind5.model.action.ActionStd;
import br.com.mind5.model.action.commom.ActionLazyCommom;
import br.com.mind5.model.action.commom.ActionStdCommom;
import br.com.mind5.model.checker.ModelChecker;
import br.com.mind5.model.checker.ModelCheckerHelperQueue;
import br.com.mind5.model.checker.ModelCheckerOption;
import br.com.mind5.model.decisionTree.DeciTreeOption;
import br.com.mind5.model.decisionTree.DeciTreeTemplateWrite;

public final class BankaccRootUpdatePartner extends DeciTreeTemplateWrite<BankaccInfo> {
	
	public BankaccRootUpdatePartner(DeciTreeOption<BankaccInfo> option) {
		super(option);
	}
	
	
	
	@Override protected ModelChecker<BankaccInfo> buildCheckerHook(DeciTreeOption<BankaccInfo> option) {
		List<ModelChecker<BankaccInfo>> queue = new ArrayList<>();		
		ModelChecker<BankaccInfo> checker;
		ModelCheckerOption checkerOption;
		
		checkerOption = new ModelCheckerOption();
		checkerOption.conn = option.conn;
		checkerOption.schemaName = option.schemaName;
		checkerOption.expectedResult = ModelCheckerOption.SUCCESS;	
		checker = new BankaccCheckUpdatePartner(checkerOption);
		queue.add(checker);
		
		return new ModelCheckerHelperQueue<>(queue);
	}
	
	
	
	@Override protected List<ActionStd<BankaccInfo>> buildActionsOnPassedHook(DeciTreeOption<BankaccInfo> option) {
		List<ActionStd<BankaccInfo>> actions = new ArrayList<>();

		ActionStd<BankaccInfo> mergeToUpdate = new ActionStdCommom<BankaccInfo>(option, BankaccVisiMergeToUpdatePartner.class);
		ActionLazy<BankaccInfo> update = new ActionLazyCommom<BankaccInfo>(option, BankaccVisiRootUpdate.class);
		
		mergeToUpdate.addPostAction(update);
		
		actions.add(mergeToUpdate);
		return actions;
	}
}
